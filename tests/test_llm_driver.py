from __future__ import annotations

import json
import tempfile
import unittest
from pathlib import Path
from unittest.mock import MagicMock, patch

from scripts.llm_driver import _check_model_available, call_ollama, run


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _make_stream_response(tokens: list[str]) -> MagicMock:
    """Build a mock HTTP response that yields streamed Ollama JSON lines."""
    lines = [
        json.dumps({"response": t, "done": False}).encode() for t in tokens
    ]
    lines.append(json.dumps({"response": "", "done": True}).encode())

    mock_resp = MagicMock()
    mock_resp.__iter__ = MagicMock(return_value=iter(lines))
    mock_resp.__enter__ = MagicMock(return_value=mock_resp)
    mock_resp.__exit__ = MagicMock(return_value=False)
    return mock_resp


def _make_tags_response(models: list[str]) -> MagicMock:
    """Build a mock HTTP response for /api/tags."""
    body = json.dumps({"models": [{"name": m} for m in models]}).encode()
    mock_resp = MagicMock()
    mock_resp.read = MagicMock(return_value=body)
    mock_resp.__enter__ = MagicMock(return_value=mock_resp)
    mock_resp.__exit__ = MagicMock(return_value=False)
    return mock_resp


# ---------------------------------------------------------------------------
# _check_model_available
# ---------------------------------------------------------------------------

class CheckModelAvailableTests(unittest.TestCase):
    def test_passes_when_model_listed(self) -> None:
        with patch("urllib.request.urlopen", return_value=_make_tags_response(["qwen2.5:14b"])):
            _check_model_available("qwen2.5:14b")  # should not raise

    def test_raises_when_model_missing(self) -> None:
        with patch("urllib.request.urlopen", return_value=_make_tags_response(["llama3.1:8b"])):
            with self.assertRaises(ValueError) as ctx:
                _check_model_available("qwen2.5:14b")
        self.assertIn("qwen2.5:14b", str(ctx.exception))
        self.assertIn("llama3.1:8b", str(ctx.exception))

    def test_raises_connection_error_when_ollama_unreachable(self) -> None:
        from urllib.error import URLError
        with patch("urllib.request.urlopen", side_effect=URLError("refused")):
            with self.assertRaises(ConnectionError):
                _check_model_available("qwen2.5:14b")


# ---------------------------------------------------------------------------
# call_ollama
# ---------------------------------------------------------------------------

class CallOllamaTests(unittest.TestCase):
    def test_assembles_streamed_tokens(self) -> None:
        tokens = ["Hello", " world", "!"]
        with patch("urllib.request.urlopen", return_value=_make_stream_response(tokens)):
            result = call_ollama("test prompt", "qwen2.5:14b")
        self.assertEqual(result, "Hello world!")

    def test_empty_response(self) -> None:
        with patch("urllib.request.urlopen", return_value=_make_stream_response([])):
            result = call_ollama("test prompt", "qwen2.5:14b")
        self.assertEqual(result, "")


