class RunTests(unittest.TestCase):
    def setUp(self) -> None:
        self.temp_dir = tempfile.TemporaryDirectory()
        self.root = Path(self.temp_dir.name)

        for relative in [
            "compiled/topics",
            "compiled/source_summaries",
            "compiled/concepts",
            "outputs/answers",
            "outputs/reports",
            "metadata/prompts",
            "metadata",
            "tmp",
        ]:
            (self.root / relative).mkdir(parents=True, exist_ok=True)

        (self.root / "metadata" / "topic-registry.json").write_text(
            json.dumps({"topics": []}), encoding="utf-8"
        )

        self.prompt_pack_path = self.root / "metadata" / "prompts" / "compile-test-topic.md"
        self.prompt_pack_path.write_text(MINIMAL_PROMPT_PACK, encoding="utf-8")

    def tearDown(self) -> None:
        self.temp_dir.cleanup()

    def _patch_run(self, synthesis_text: str, model: str = "qwen2.5:14b") -> int:
        """Run the driver with mocked Ollama calls and a patched ROOT."""
        tokens = [synthesis_text]

        with (
            patch("scripts.llm_driver.ROOT", self.root),
            patch("scripts.llm_driver.TMP_OUTPUT", self.root / "tmp" / "synthesis-output.md"),
            patch("urllib.request.urlopen") as mock_urlopen,
        ):
            # First urlopen call → /api/tags, second → /api/generate
            mock_urlopen.side_effect = [
                _make_tags_response([model]),
                _make_stream_response(tokens),
            ]
            return run(
                prompt_pack=self.prompt_pack_path,
                model=model,
                output_type="compiled",
                title_override="",
                force=False,
            )

    def test_run_creates_artifact(self) -> None:
        rc = self._patch_run(MINIMAL_SYNTHESIS)
        self.assertEqual(rc, 0)
        artifact = self.root / "compiled" / "topics" / "test-topic.md"
        self.assertTrue(artifact.exists(), f"Expected artifact at {artifact}")

    def test_run_sets_generation_method_ollama_local(self) -> None:
        self._patch_run(MINIMAL_SYNTHESIS)
        artifact = self.root / "compiled" / "topics" / "test-topic.md"
        content = artifact.read_text(encoding="utf-8")
        self.assertIn('generation_method: "ollama_local"', content)

    def test_run_saves_tmp_file(self) -> None:
        self._patch_run(MINIMAL_SYNTHESIS)
        tmp = self.root / "tmp" / "synthesis-output.md"
        self.assertTrue(tmp.exists())
        self.assertIn("Synthesized content", tmp.read_text(encoding="utf-8"))

    def test_run_returns_error_when_prompt_pack_missing(self) -> None:
        rc = run(
            prompt_pack=self.root / "metadata" / "prompts" / "nonexistent.md",
            model="qwen2.5:14b",
            output_type="compiled",
            title_override="",
            force=False,
        )
        self.assertEqual(rc, 1)

    def test_run_returns_error_when_model_unavailable(self) -> None:
        with (
            patch("scripts.llm_driver.ROOT", self.root),
            patch("urllib.request.urlopen", return_value=_make_tags_response(["llama3.1:8b"])),
        ):
            rc = run(
                prompt_pack=self.prompt_pack_path,
                model="qwen2.5:14b",
                output_type="compiled",
                title_override="",
                force=False,
            )
        self.assertEqual(rc, 1)

    def test_run_force_overwrites_existing_artifact(self) -> None:
        self._patch_run(MINIMAL_SYNTHESIS)
        # Second run without force should fail
        rc = self._patch_run(MINIMAL_SYNTHESIS)
        self.assertEqual(rc, 1)
        # Third run with force should succeed
        with (
            patch("scripts.llm_driver.ROOT", self.root),
            patch("scripts.llm_driver.TMP_OUTPUT", self.root / "tmp" / "synthesis-output.md"),
            patch("urllib.request.urlopen") as mock_urlopen,
        ):
            mock_urlopen.side_effect = [
                _make_tags_response(["qwen2.5:14b"]),
                _make_stream_response([MINIMAL_SYNTHESIS]),
            ]
            rc = run(
                prompt_pack=self.prompt_pack_path,
                model="qwen2.5:14b",
                output_type="compiled",
                title_override="",
                force=True,
            )
        self.assertEqual(rc, 0)


if __name__ == "__main__":
    unittest.main()
